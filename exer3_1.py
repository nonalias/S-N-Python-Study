# ---------------------------------------------
# 2,4,6,8,10 을 요소로 가진 튜플 t를 정의한다.
# ---------------------------------------------
t=(2,4,6,8,10)
print('2,4,6,8,10 을 요소로 가진 튜플 t를 정의한다. : '+str(t))

# ----------------------------------------------
# 튜플 t의 두번째 요소를 출력하라.
# ----------------------------------------------
print('튜플 t의 두번째 요소를 출력하라. : '+str(t[2]))

# ----------------------------------------------
# 10이라는 값이 있는 위치를 출력하라.
# ----------------------------------------------
print('10이라는 값이 있는 위치를 출력하라. : '  +str(t.index(10)))


# ----------------------------------------------
# t의 길이를 출력하라
# ----------------------------------------------
print('t의 길이를 출력하라 : '+str(len(t)))


# ----------------------------------------------------
# 2번째 요소부터 마지막 앞까지 슬라이싱하여 출력하라.
# ----------------------------------------------------
print('2번째 요소부터 마지막 앞까지 슬라이싱하여 출력하라. : '+str(t[2:-1]))



# --------------------------------------------------------
# t를 처음부터 1번째요소 앞까지 슬라이싱하여 출력하라.
# --------------------------------------------------------
print('t를 처음부터 1번째요소 앞까지 슬라이싱하여 출력하라. : '+str(t[:1]))